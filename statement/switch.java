public class IfExample 
{
	public static void main(String[] args)
	{
		int age=20;
		switch(age)
		{
			case 10:
			System.out.println("Age is Little");
			break;
			case 20:
			System.out.println("Age is Not Little");
			break;
			case 30:
			System.out.println("Age is Too Little");
			break;
			case 40:
			System.out.println("Age is late Little");
			break;
			case 10:
			System.out.println("Age is Little");
			break;
			default:
			System.out.println("Age is Perfect");
			break;
		}
		
	}
}